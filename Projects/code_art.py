t.color("blue")
t.pendown()

t.forward(100)
t.left(50)
t.forward(100)
t.left(50)
t.forward(100)
t.left(50)
t.forward(100)
t.left(50)
t.forward(100)
t.left(50)


# ###############################################
# ### ENDING ###
turtle.exitonclick()
